set_one = {1, 2, 3, 4, 5}
set_two = {4, 5, 6, 7, 8}

union_set = set_one.union(set_two)
print("Here is the union set: " + str(union_set))

intersection_set = set_one.intersection(set_two)
print("Here is the intersection set: " + str(intersection_set))

difference_set = set_one.difference(set_two)
print("Here is the difference set: " + str(difference_set))